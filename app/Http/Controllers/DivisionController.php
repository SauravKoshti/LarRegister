     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        //
        $division = Division::find($id);
        $division->delete();
        return redirect('divisions')->with('success', 'Division Successfully deleted!');
    }
